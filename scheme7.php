<br>
The financial figures to assist the issue include Rs. 50 crore for testing a scheme for safety of women on Public Road Transport by The Ministry of Road Transport and Highways. Additionally, Rs.150 crore will be spent by the Ministry of Home Affairs to increase the safety of women in large cities.

From the Nirbhaya fund, there is a proposal to set up Crisis Management Centres this year, in all districts of the capital, Delhi. The centres will be set up in all Government and Private Hospitals.
