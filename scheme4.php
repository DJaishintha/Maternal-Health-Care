Chhattisgarh Swarna Jayanti Rozgar Yojana
The Chief Minister said that earlier Swarna Jayanti Swarozgar Yojana was implemented in the villages for livelihood and self-reliance. In which there was equal participation of women and men. Now the state government has changed its name as Bihan Yojana (सुबह का मतलब) and now this scheme is completely devoted to women. Under this, women self-help groups are being formed.

<br>
 
The Chief Minister said that more than one lakh 24 thousand self-help groups have been set up under the State Rural Livelihood Mission. Chhattisgarh government has decided to provide easy and cheap loans to women self-help groups. The State Government has also provided easy and low interest loans to women self-help groups. In the last 5 years, the State Government has distributed a loan amount of about Rs 1,151 crore for about 90,000 SHGs.
<br>
Women have received National Level Acknowledgment for SHG for their work in solid waste management. These groups collect solid waste, process it and convert it into fertilizers, which can be sold for income. Nearly 44 block women have become bankers in 20 districts. Under this scheme, the SHG provides door bank facility from door to door. This Bihaan scheme will strengthen women living villages and rural areas.

<br>
 
Benefits of Chhattisgarh Swarna Jayanti Rozgar Yojana / Bihaan Yojana
The women SHG promotes employment oriented and income generating activities.
SHG now produces rice through organic farming which is in great demand in metro cities.
Some SHGs have started producing LED bulbs instead of producing candles.
Women have received a national level receipt for their work in SHG for solid waste management.
