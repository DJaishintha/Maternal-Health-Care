BENEFITS OF THE SCHEME:
<br>
In this scheme, the government will give medical care to the pregnent women and her child.
The government will provide financial help of Rs. 1000 to the pregnent women to reach safly to the hospital at the time of delivery.
The the benefit of this scheme will be directly provided to the pregnent women.
About 60,000 women will be benefited under this scheme who living in remote areas where the Ambulance service is not available.<br>
Besides, 47,000 Asha workers will also be benefited in this scheme.
The government has targeted to cover 7853 villages in the state.
  <br>
  *Application Process for Incentive – Odisha Sampurna Yojana

The family members of patient can submit the filled application form at the hospital during admit the pregnent women.
