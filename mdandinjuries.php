        <h2 style=" font-size:30px;font-weight: bold;" class="display-4"> Description</h2>
      <p style="font-family: cursive;"class="py-3"> Maternal death or maternal mortality is defined by the World Health Organization (WHO) as "the death of a woman while pregnant or within 42 days of termination of pregnancy, irrespective of the duration and site of the pregnancy, from any cause related to or aggravated by the pregnancy or its management but not from accidental or incidental causes."[1][2]

Adding to the WHO definition, the CDC extends the period of consideration to include up to 1 year within the end of a pregnancy regardless of the outcome.[3]

There are two performance indicators that are sometimes used interchangeably: maternal mortality ratio and maternal mortality rate, which confusingly both are abbreviated "MMR".[4] By 2017, the world maternal mortality rate had declined 44% since 1990, but still every day 830 women die from pregnancy or childbirth related causes.[5] According to the United Nations Population Fund (UNFPA) 2017 report, this is equivalent to "about one woman every two minutes and for every woman who dies, 20 or 30 encounter complications with serious or long-lasting consequences. Most of these deaths and injuries are entirely preventable."[5]

UNFPA estimated that 303,000 women died of pregnancy or childbirth related causes in 2015.[5] These causes range from severe bleeding to obstructed labour,[6] for which there are highly effective interventions. As women have gained access to family planning and skilled birth attendance with backup emergency obstetric care, the global maternal mortality ratio has fallen from 385 maternal deaths per 100,000 live births in 1990 to 216 deaths per 100,000 live births in 2015, and many countries halved their maternal death rates in the last 10 years.[5]

Although attempts have been made in reducing maternal mortality, there is much room for improvement, particularly in impoverished regions. Over 85% of maternal deaths are from impoverished communities in Africa and Asia.[5] The effect of a mother's death results in vulnerable families. Their infants, if they survive childbirth, are more likely to die before reaching their second birthday.[5].</p>


