Factors that increase maternal death can be direct or indirect. In a 2009 article on maternal morbidity, the authors said, that generally, there is a distinction between a direct maternal death that is the result of a complication of the pregnancy, delivery, or management of the two, and an indirect maternal death,[7] that is a pregnancy-related death in a patient with a preexisting or newly developed health problem unrelated to pregnancy. Fatalities during but unrelated to a pregnancy are termed accidental, incidental, or nonobstetrical maternal deaths.
<br><br><br>
According to a study published in the Lancet which covered the period from 1990 to 2013, the most common causes are postpartum bleeding (15%), complications from unsafe abortion (15%), hypertensive disorders of pregnancy (10%), postpartum infections (8%), and obstructed labour (6%).[6] Other causes include blood clots (3%) and pre-existing conditions (28%).[8] Maternal mortality caused by severe bleeding and infections are mostly after childbirth. Indirect causes are malaria, anaemia,[9] HIV/AIDS, and cardiovascular disease, all of which may complicate pregnancy or be aggravated by it[citation needed]. Risk factors associated with increased maternal death include the age of the mother, obesity before becoming pregnant, other pre-existing chronic medical conditions, and cesarean delivery.[10][11]<br><br><br>

